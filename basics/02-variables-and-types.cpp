#include <iostream>
using namespace std;

int main() {
    // Integer variable
    int age = 25;
    
    // Character variable
    char initial = 'M';
    
    // String variable
    string name = "Mahmoud";
    
    // Floating point variables
    float height = 5.9f;
    double salary = 50000.50;
    
    // Boolean variable
    bool isStudent = true;
    
    // Constant variable
    const int daysInWeek = 7;
    
    // Display values
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Initial: " << initial << endl;
    cout << "Height: " << height << " ft" << endl;
    cout << "Salary: " << salary << endl;
    cout << "Is Student: " << isStudent << endl;
    cout << "Days in Week: " << daysInWeek << endl;
    
    return 0;
}
